let darkPink = [1.0, 0.0, 1.0, 1.0];
let orange = [1.0, 0.5, 0.0, 1.0];
let blue = [0.0, 1.0, 1.0, 1.0];
let black = [0.1, 0.1, 0.1, 1.0];

function drawLynnPictureShapes() {
  let shapeGroups = [
    [bgTris, blue],
    [darkPinkTris_L, darkPink],
    [orangeTris_L, orange],
    [darkPinkTris_G, darkPink],
    [orangeTris_G, orange],
    [eyesTris, black]
  ]

  // clear canvas first
  g_shapesList = [];

  // draw each group of shapes
  for (let i = 0; i < shapeGroups.length; i++) {
    let vertexArr = shapeGroups[i][0];
    let color = shapeGroups[i][1];

    // modified Triangle class to take in external vertices, and draw as many tris as possible with that list
    let tri = new Triangle();
    tri.vertices = vertexArr.slice();
    tri.color = color.slice();  // slice to send copy

    g_shapesList.push(tri);
  }

  renderAllShapes();
}